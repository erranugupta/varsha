			<p><strong>Butch&#8217;s Auto Body &amp; Painting, Inc.</strong> is locally-owned and operated full service auto body repair &amp; painting company.  Located in <em><a title="Find/Contact Butchs Auto Body" href="http://www.butchsautobody.com/find-us/">Capital Heights, Maryland</a> </em>and has been servicing the Metro DC area for more than 40 years.  We strive to achieve complete customer satisfaction, and we have earned a solid reputation for providing reliable, high-quality services.  You can count on us for all your auto body and collision repair needs.</p>
<p>It is possible to restore your peace of mind after an accident.  The key is to enter the collision repair process with confidence.  When you go to a repair shop like <em>Butchs Auto Body</em>, you know its backed by a company thats been bringing technologically advanced collision repair service with our state-of-the-art new equipment and highly trained technicians from certifying agencies as the <a href="https://www.ase.com/Home.aspx" target="_blank">Automotive Service of Excellence (ASE)</a> and <a href="http://i-car.com/" target="_blank">Industry Conference on Auto Collision Repair (I-CAR).</a>  We demand the highest standards in workmanship and quality and our customers come first!</p>
<p>Butchs Auto Body &amp; Painting, Inc. is working to improve every aspect of the collision repair industry.  We dont offer one-size-fits-all services.  We build a program that meets the needs of individual customers.  Butchs Auto Body builds confidence and takes the guess work out of collision repair, so you can relax and get your life back to normal.</p>
<p>Butch&#8217;s Auto Body &amp; Painting, Inc. provides complete collision repair needs to Capital Heights, Upper Marlboro, Forestville, Mitchellville, District Heights, Lanham, Bowie, Clinton, Walker Mill, Landover, Largo, Washington DC and surrounding MD communities.</p>
